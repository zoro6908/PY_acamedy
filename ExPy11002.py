# 딕션너리 : 변수 = {키:값}
my_dict = {'한국':60, '일본':40, '중국':50}
print(my_dict['한국'])                             # 60
my_dict['중국'] = 34                               # 요소 값 변경 가능(키를 이용한 접근)
print(my_dict)                                    # {'한국': 60, '일본': 40, '중국': 34}